function createNieuwsArtikel(titelArt, subtext, foto, id, type, col) {
    let nieuwsDiv = document.createElement("div");
    nieuwsDiv.className = col + "p-2 mb-4 nieuwsartikel " + type;
    nieuwsDiv.id = id;

    // let linkDetail = document.createElement("a");
    // linkDetail.href = "";

    let imageDiv = document.createElement("div");
    let image = document.createElement("img");
    image.src = "assets/" + foto;
    imageDiv.appendChild(image);

    let titel = document.createElement("h6");
    titel.className = "mt-4";
    titel.textContent = titelArt;

    let paragraph = document.createElement("p");
    paragraph.textContent = subtext;

    nieuwsDiv.appendChild(imageDiv);
    nieuwsDiv.appendChild(titel);
    nieuwsDiv.appendChild(paragraph);
    // linkDetail.appendChild(imageDiv);
    // linkDetail.appendChild(titel);
    // linkDetail.appendChild(paragraph);

    // nieuwsDiv.appendChild(linkDetail);

    let lijst = document.getElementById("nieuwslijst");
    lijst.appendChild(nieuwsDiv);
}

function maakNieuwsLijst(lijst){
    let col = "";

    for (let i = 0; i<lijst.length; i++){
        if(i == 0 || i == 5){
            col = "col-xl-8 ";
        } else{
            col = "col-xl-4 col-md-6 ";
        }
        let artikel = lijst[i];
        createNieuwsArtikel(artikel.titel, artikel.subtext, artikel.hoofdfoto, "artikel" + i, artikel.type, col);
    }
}

function filterArtikels(filter){
    let lijst = document.getElementsByClassName("nieuwsartikel");

    for(let i = 0; i < lijst.length; i++){
        let artikel = lijst[i];
        artikel.style.display = "block";

        if(filter === "all"){
            artikel.style.display = "block";
        } else{
            if(!artikel.classList.contains(filter)){
                artikel.style.display = "none";
            }
        }
    }
    let categorie = document.getElementById("nieuws__categorie").children;
    let vorige = document.getElementById("activeCat");
    vorige.id = "";

    switch (filter){
        case "all":
            categorie[0].id = "activeCat";
            break;
        case "ondernemingsrecht":
            categorie[1].id = "activeCat";
            break;
        case "verkoopsrecht":
            categorie[2].id = "activeCat";
            break;
        case "fusiesOvername":
            categorie[3].id = "activeCat";
            break;
    }
}

